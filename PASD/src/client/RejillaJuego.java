    private void sendResp(int click) {

        Socket s = null;
        try {

            s = new Socket(TCPA, TCPP);	

            in = new ObjectInputStream( s.getInputStream());
            out = new ObjectOutputStream( s.getOutputStream());
            
            Respuesta message = new Respuesta(MonsterClient.partida, click, jugador);

            out.writeObject(message); 
            System.out.println("Mandando respuesta");
            

        } 
        catch (UnknownHostException e) {
            System.out.println("Sock:"+e.getMessage()); 
        }
        catch (EOFException e) {
            System.out.println("EOF:"+e.getMessage());
        } 
        catch (IOException e) {
            System.out.println("IO:"+e.getMessage());
        }
            finally {
            if(s!=null) 
                try {
                    s.close();
                } catch (IOException e){
                System.out.println("close:"+e.getMessage());}
        }
    }

    public void borrarRegistro() {
        System.setProperty("java.security.policy","/Users/fernandasramirezm/NetBeansProjects/PASD/src/client/client.policy");

        if (System.getSecurityManager() == null) {
            System.setSecurityManager(new SecurityManager());
        }

      
        
        mc.stop();
        Registry registry;
        try {
            registry = LocateRegistry.getRegistry("localhost"); // server's ip address
            Compute gameManager = (Compute) registry.lookup("MonsterServer");
            gameManager.Exit(jugador);
            IniciaJuego ventana = new IniciaJuego();
            ventana.setVisible(true);
            super.dispose();
        } catch (RemoteException | NotBoundException ex) {
            Logger.getLogger(IniciaJuego.class.getName()).log(Level.SEVERE, null, ex);
        }    
    }
    
    private void cerrar() {
        setDefaultCloseOperation(javax.swing.WindowConstants.DO_NOTHING_ON_CLOSE);
 
        addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent evt) {
                borrarRegistro();
                System.exit(0);
            }
        });
    }
    
